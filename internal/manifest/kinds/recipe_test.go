// ── 8. ExportRecipes: only installed rows round-trip ──────────────────────

func TestExportRecipes_OnlyInstalled(t *testing.T) {
	client := newFakeClient()
	client.recipes["code-review"] = RecipeRemote{
		ID: "rec_a", Slug: "code-review", Installed: true,
	}
	client.recipes["research"] = RecipeRemote{
		ID: "rec_b", Slug: "research", Installed: false,
	}
	client.recipes["analytics"] = RecipeRemote{
		ID: "rec_c", Slug: "analytics", Installed: true,
	}

	docs, err := ExportRecipes(context.Background(), client)
	if err != nil {
		t.Fatalf("ExportRecipes: %v", err)
	}
	if len(docs) != 2 {
		t.Fatalf("want 2 installed docs, got %d", len(docs))
	}
	seen := map[string]bool{}
	for _, d := range docs {
		if d.Kind != "Recipe" {
			t.Errorf("kind = %q, want Recipe", d.Kind)
		}
		if d.APIVersion != "crewship/v1" {
			t.Errorf("apiVersion = %q, want crewship/v1", d.APIVersion)
		}
		if !d.Spec.Install {
			t.Errorf("export should set Install=true, slug=%s", d.Metadata.Slug)
		}
		seen[d.Metadata.Slug] = true
	}
	if !seen["code-review"] || !seen["analytics"] {
		t.Errorf("missing expected slugs in export: %v", seen)
	}
	if seen["research"] {
		t.Error("uninstalled recipe should NOT appear in export")
	}
}
