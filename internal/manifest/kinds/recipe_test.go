// fakeClient implements internalapi.Client over a tiny route table.
// One concrete recipe under management at a time is enough — each
// test case configures the slug + installed flag it cares about.
type fakeClient struct {
	wsID string

	// recipes is the catalog the fake "server" exposes. Keyed by
	// slug; values carry the installed flag the GET handlers return.
	recipes map[string]RecipeRemote

	// installCallback runs whenever POST /api/v1/recipes/{slug}/install
	// fires. Tests set this to capture the body the manifest sent.
	installCallback func(slug string, body any)

	// installStatus is the HTTP status the install endpoint returns
	// (defaults to 201).
	installStatus int

	// catalog404 forces the GET /api/v1/recipes/{slug} handler to
	// return 404 — used to test the "recipe slug missing from catalog"
	// path through LookupRecipeRemote.
	catalog404 map[string]bool

	// calls records every HTTP method+path so tests can assert no
	// extraneous traffic was generated by Plan for unchanged cases.
	calls []string
}

func newFakeClient() *fakeClient {
	return &fakeClient{
		wsID:       "ws_test",
		recipes:    map[string]RecipeRemote{},
		catalog404: map[string]bool{},
	}
}

func (f *fakeClient) WorkspaceID() string { return f.wsID }

func (f *fakeClient) record(method, path string) {
	f.calls = append(f.calls, method+" "+path)
}

func jsonResp(status int, v any) *internalapi.Response {
	data, _ := json.Marshal(v)
	return &internalapi.Response{
		StatusCode: status,
		Body:       io.NopCloser(bytes.NewReader(data)),
	}
}

func (f *fakeClient) Get(_ context.Context, path string) (*internalapi.Response, error) {
	f.record("GET", path)
	switch {
	case path == "/api/v1/recipes":
		var out []RecipeRemote
		for _, r := range f.recipes {
			out = append(out, r)
		}
		return jsonResp(200, out), nil
	case strings.HasPrefix(path, "/api/v1/recipes/"):
		slug := strings.TrimPrefix(path, "/api/v1/recipes/")
		if f.catalog404[slug] {
			return jsonResp(404, map[string]any{"error": "Recipe not found"}), nil
		}
		row, ok := f.recipes[slug]
		if !ok {
			return jsonResp(404, map[string]any{"error": "Recipe not found"}), nil
		}
		return jsonResp(200, row), nil
	}
	return jsonResp(404, map[string]any{"error": "not found"}), nil
}

func (f *fakeClient) Post(_ context.Context, path string, body any) (*internalapi.Response, error) {
	f.record("POST", path)
	if strings.HasPrefix(path, "/api/v1/recipes/") && strings.HasSuffix(path, "/install") {
		slug := strings.TrimSuffix(strings.TrimPrefix(path, "/api/v1/recipes/"), "/install")
		if f.installCallback != nil {
			f.installCallback(slug, body)
		}
		// Mutate the fake server's installed flag so re-Planning
		// against the same client returns Unchanged.
		row := f.recipes[slug]
		row.Installed = true
		row.Slug = slug
		f.recipes[slug] = row
		status := f.installStatus
		if status == 0 {
			status = 201
		}
		return jsonResp(status, map[string]any{"crew_slug": "demo"}), nil
	}
	return jsonResp(404, map[string]any{"error": "not found"}), nil
}

func (f *fakeClient) Patch(_ context.Context, _ string, _ any) (*internalapi.Response, error) {
	return jsonResp(404, nil), nil
}

func (f *fakeClient) Put(_ context.Context, _ string, _ any) (*internalapi.Response, error) {
	return jsonResp(404, nil), nil
}

func (f *fakeClient) Delete(_ context.Context, path string) (*internalapi.Response, error) {
	f.record("DELETE", path)
	return jsonResp(404, nil), nil
}

