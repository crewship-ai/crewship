// ── 3. Plan: install=true, remote nil → Create ────────────────────────────

func TestRecipe_Plan_InstallWhenAbsent(t *testing.T) {
	doc := &RecipeDocument{
		Metadata: internalapi.Metadata{Name: "Code Review", Slug: "code-review"},
		Spec: RecipeSpec{
			Install: true,
			Inputs: map[string]any{
				"credential_values": map[string]any{"ANTHROPIC_API_KEY": "sk-test"},
			},
		},
	}
	client := newFakeClient()
	var capturedSlug string
	var capturedBody map[string]any
	client.installCallback = func(slug string, body any) {
		capturedSlug = slug
		if m, ok := body.(map[string]any); ok {
			capturedBody = m
		}
	}

	items, err := doc.Plan(context.Background(), client, nil)
	if err != nil {
		t.Fatalf("Plan: %v", err)
	}
	if len(items) != 1 {
		t.Fatalf("want 1 item, got %d", len(items))
	}
	if items[0].Action != internalapi.ActionCreate {
		t.Errorf("want ActionCreate, got %s", items[0].Action)
	}
	if items[0].Exec == nil {
		t.Fatal("Create item must have non-nil Exec")
	}
	if err := items[0].Exec(context.Background(), client); err != nil {
		t.Fatalf("Exec: %v", err)
	}
	if capturedSlug != "code-review" {
		t.Errorf("want install slug code-review, got %q", capturedSlug)
	}
	if capturedBody == nil {
		t.Fatal("install body was nil")
	}
	if _, ok := capturedBody["credential_values"]; !ok {
		t.Errorf("install body missing credential_values: %v", capturedBody)
	}
}

// ── 4. Plan: install=true, remote.Installed → Unchanged ───────────────────

func TestRecipe_Plan_InstallWhenAlreadyInstalled(t *testing.T) {
	doc := &RecipeDocument{
		Metadata: internalapi.Metadata{Slug: "code-review"},
		Spec:     RecipeSpec{Install: true},
	}
	remote := &RecipeRemote{Slug: "code-review", Installed: true}

	client := newFakeClient()
	items, err := doc.Plan(context.Background(), client, remote)
	if err != nil {
		t.Fatalf("Plan: %v", err)
	}
	if len(items) != 1 || items[0].Action != internalapi.ActionUnchanged {
		t.Fatalf("want single Unchanged, got %+v", items)
	}
	if items[0].Exec != nil {
		t.Error("Unchanged items must have nil Exec")
	}
	if !strings.Contains(items[0].Description, "already installed") {
		t.Errorf("want already-installed description, got %q", items[0].Description)
	}
	// No HTTP traffic should fire for an Unchanged plan.
	if len(client.calls) != 0 {
		t.Errorf("Unchanged plan should issue zero requests, got %v", client.calls)
	}
}

