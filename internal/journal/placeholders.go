package journal

import "strings"

// sqlInPlaceholders returns the inside-parens body of an SQL IN-clause
// for n placeholders — e.g. "?,?,?" for n=3, "" for n=0. The caller
// wraps it with parens to form "IN (?,?,?)".
//
// Small n hits a precomputed table so the common UI-filter case
// (rarely more than ~16 elements) is allocation-free. Larger n falls
// back to a single strings.Builder. Replaces the previous
// `strings.Repeat("?,", n)` + slice-trim + concat pattern repeated
// across queries.go and runs.go.
func sqlInPlaceholders(n int) string {
	if n <= 0 {
		return ""
	}
	if n < len(sqlInPlaceholdersTable) {
		return sqlInPlaceholdersTable[n]
	}
	var b strings.Builder
	b.Grow(2*n - 1)
	b.WriteByte('?')
	for i := 1; i < n; i++ {
		b.WriteString(",?")
	}
	return b.String()
}

var sqlInPlaceholdersTable = [...]string{
	"",
	"?",
	"?,?",
	"?,?,?",
	"?,?,?,?",
	"?,?,?,?,?",
	"?,?,?,?,?,?",
	"?,?,?,?,?,?,?",
	"?,?,?,?,?,?,?,?",
	"?,?,?,?,?,?,?,?,?",
	"?,?,?,?,?,?,?,?,?,?",
	"?,?,?,?,?,?,?,?,?,?,?",
	"?,?,?,?,?,?,?,?,?,?,?,?",
	"?,?,?,?,?,?,?,?,?,?,?,?,?",
	"?,?,?,?,?,?,?,?,?,?,?,?,?,?",
	"?,?,?,?,?,?,?,?,?,?,?,?,?,?,?",
	"?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?",
}
